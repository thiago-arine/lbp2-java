class condicaoNao {
    public static void main(String[] args)
    {
        // inicializando variaveis
        int a = 10, b = 1;
        // Imprimindo a,b e c
        System.out.println("Var1 = " + a);
        System.out.println("Var2 = " + b);
        // Usando logica NAO
        System.out.println("!(a < b) = " + !(a < b));
        System.out.println("!(a > b) = " + !(a > b));
    }
}